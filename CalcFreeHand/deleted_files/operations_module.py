import math

def addition(n1, n2):
    return(n1+n2)

def subtraction(n1, n2):
    return(n1-n2)

def multiplication(n1, n2):
    return(n1*n2)

def division(n1, n2):
    return(n1/n2)
#expo: param 1 - numb to exponentiate, param 2 - power
def expo(n1, n2):
	return (n1**n2)

def square(n1): # needs n1 to be >= 0
	return (math.sqrt(n1))

